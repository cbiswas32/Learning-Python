#Sorting l1. It operates on l1 itself
l1=[1,8,7,2,21,15,21]
l1.sort()
print(l1)

l1.reverse() #reverses the list
print(l1)

##list.append() appends/adds to the end of the list
l1.append(45)
print(l1)

#list.insert(index,number)
l1.insert(2,544) #inserting 544 in the 2nd index of list

print(l1)


#list.pop(index)
l1.pop(2) # remove the element of index 2 
print(l1)

#list.remove(number)

l1.remove(21) # remove the element  21 (first) 
print(l1)



